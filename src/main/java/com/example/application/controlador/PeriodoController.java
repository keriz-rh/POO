    // Método para activar un periodo
    public Periodo activarPeriodo(Long periodoId) {
        // Primero desactivamos todos los periodos
        repository.findPeriodosActivos().forEach(p -> {
            p.setActivo(false);
            repository.save(p);
        });

        // Luego activamos el periodo seleccionado
        Periodo periodo = repository.findById(periodoId)
            .orElseThrow(() -> new RuntimeException("Periodo no encontrado"));
        periodo.setActivo(true);
        return repository.save(periodo);
    }
